package net.agilhard.terminal.emulation;

/**
 * The Enum Mode.
 */
enum Mode {

    /** The Null. */
    Null,

    /** The Cursor key. */
    CursorKey,

    /** The ansi. */
    ANSI,

    /** The Wide column. */
    WideColumn,

    /** The Smooth scroll. */
    SmoothScroll,

    /** The Reverse screen. */
    ReverseScreen,

    /** The Relative origin. */
    RelativeOrigin,

    /** The Wrap around. */
    WrapAround,

    /** The Auto repeat. */
    AutoRepeat,

    /** The Interlace. */
    Interlace;
}